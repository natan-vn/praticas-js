// Aula 1 - Entendendo Variaveis

let nome = "Natanael ";
let sobrenome = "vianaNogueira "; // Ao declarar variáveis em JS, é uma boa prática usar o modelo camelCase

console.log("Seu nome é " + nome + sobrenome);

let peso, idade, altura; // Você também pode atribuir diversas variáveis de uma vez (NÃO É RECOMENDADO)

let valorMutavel = "Mutável"; // Variáveis podem ser alteradas ao decorrer do código

const naoMuda = "Imutável"; // Constantes são variáveis de valor fixo, que não podem ser alteradas, a não ser que eu mude a const.

console.log("Eu sou uma variável, eu sou: " + valorMutavel);
console.log("Eu sou uma constante, eu sou: " + naoMuda);
