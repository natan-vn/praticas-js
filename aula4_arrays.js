//ARRAYS

let ingredientes = ["macarrão", "óleo", "sal", "molho", "carne"]; // Uma array nada mais é do que um conjunto de dados do mesmo tipo, organizados por um índice que começa em 0

console.log(ingredientes); // Aqui eu imprimo todos os 5 elementos da array, nesse caso, do 0 ao 4
console.log(typeof ingredientes); // Aqui é notável que uma array é tratada pelo JS como um objeto

console.log[ingredientes[1]]; // Quando específicamos o índice, podemos ter como retorno, aquele elemento que ocupa.
console.log(typeof ingredientes[1]); // Note que conseguimos também, ver o tipo de um único elemento
